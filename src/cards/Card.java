package cards;

public class Card {
    private String tl;
    private String tr;
    private String bl;
    private String br;

    @Override
    public String toString() {
        return "Card{" +
                "tl=" + tl + ' ' +
                ", tr=" + tr + ' ' +
                ", bl=" + bl + ' ' +
                ", br=" + br + ' ' +
                '}';
    }

}
